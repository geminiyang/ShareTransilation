package com.idear.move.POJO;

public class DynamicsPraiseDataModel {

    public String discussantIconUrl;
    public String username;
    public String time;
    public String picUrl;
    public String textContent;

    public DynamicsPraiseDataModel(String discussantIconUrl, String username, String time, String picUrl, String textContent) {
        this.discussantIconUrl = discussantIconUrl;
        this.username = username;
        this.time = time;
        this.picUrl = picUrl;
        this.textContent = textContent;
    }
}
